declare(strict_types=1);

namespace GravityKit\GravityRevisions\Doctrine\Inflector\Rules\NorwegianBokmal;

use GravityKit\GravityRevisions\Doctrine\Inflector\Rules\Patterns;
use GravityKit\GravityRevisions\Doctrine\Inflector\Rules\Ruleset;
use GravityKit\GravityRevisions\Doctrine\Inflector\Rules\Substitutions;
use GravityKit\GravityRevisions\Doctrine\Inflector\Rules\Transformations;

final class Rules
{
    public static function getSingularRuleset(): Ruleset
    {
        return new Ruleset(
            new Transformations(...Inflectible::getSingular()),
            new Patterns(...Uninflected::getSingular()),
            (new Substitutions(...Inflectible::getIrregular()))->getFlippedSubstitutions()
        );
    }

    public static function getPluralRuleset(): Ruleset
    {
        return new Ruleset(
            new Transformations(...Inflectible::getPlural()),
            new Patterns(...Uninflected::getPlural()),
            new Substitutions(...Inflectible::getIrregular())
        );
    }
}
